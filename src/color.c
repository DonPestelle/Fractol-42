#include "fractol.h"

int	colorize(int it)
{
	double	t;
	int		r;
	int		g;
	int		b;

	if (it >= MAX_IT)
		return (0x000000);
	t = (double)it / (double)MAX_IT;
	r = (int)(sin(t * 3.14159 * 2.0 + 0.0) * 127 + 128);
	g = (int)(sin(t * 3.14159 * 2.0 + 2.094) * 127 + 128);
	b = (int)(sin(t * 3.14159 * 2.0 + 4.188) * 127 + 128);
	return ((r << 16) | (g << 8) | b);
}
